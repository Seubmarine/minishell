#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (s1[i] == s2[i] && (s1[i] != '\0'))
	{
		i++;
	}
	if (s1[i] > s2[i])
	{
		a = s1[i] - s2[i];
		return (a);
	}
	else
	{
		a = s1[i] - s2[i];
		return (a);
	}
	return (a);
}
